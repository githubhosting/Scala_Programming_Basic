// Arithmetic Operators
val addition = 10 + 5 // Addition operator
val subtraction = 10 - 5 // Subtraction operator
val multiplication = 10 * 5 // Multiplication operator
val division = 10 / 5 // Division operator
val modulus = 10 % 5 // Modulus operator

// Relational Operators
val greaterThan = 10 > 5 // Greater than operator
val lessThan = 10 < 5 // Less than operator
val greaterThanOrEqual = 10 >= 5 // Greater than or equal to operator
val lessThanOrEqual = 10 <= 5 // Less than or equal to operator
val equalTo = 10 == 5 // Equality operator
val notEqualTo = 10 != 5 // Not equal to operator

// Logical Operators
val logicalAnd = true && false // Logical AND operator
val logicalOr = true || false // Logical OR operator
val logicalNot = !true // Logical NOT operator

// Bitwise Operators
val bitwiseAnd = 10 & 5 // Bitwise AND operator
val bitwiseOr = 10 | 5 // Bitwise OR operator
val bitwiseXor = 10 ^ 5 // Bitwise XOR operator
val bitwiseComplement = ~10 // Bitwise complement operator
val leftShift = 10 << 2 // Left shift operator
val rightShift = 10 >> 2 // Right shift operator
val zeroFillRightShift = 10 >>> 2 // Zero-fill right shift operator

// Assignment Operators
var x = 10 // Simple assignment operator
x += 5 // Addition assignment operator
x -= 5 // Subtraction assignment operator
x *= 5 // Multiplication assignment operator
x /= 5 // Division assignment operator
x %= 5 // Modulus assignment operator
x &= 5 // Bitwise AND assignment operator
x |= 5 // Bitwise OR assignment operator
x ^= 5 // Bitwise XOR assignment operator
x <<= 2 // Left shift assignment operator
x >>= 2 // Right shift assignment operator
x >>>= 2 // Zero-fill right shift assignment operator

// Other Operators
val conditionalOperator = if (x > 5) "x is greater than 5" else "x is less than or equal to 5" // Conditional operator
val rangeOperator = 1 to 10 // Range operator

// Unary Operators
val unaryPlus = +x // Unary plus operator
val unaryMinus = -x // Unary minus operator
val unaryNot = !true // Unary NOT operator

// Ternary Operator (Not available in Scala)
val ternaryOperator = if (x > 5) "x is greater than 5" else "x is less than or equal to 5"
