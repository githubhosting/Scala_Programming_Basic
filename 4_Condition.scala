// Example 1: If-else statement
val x = 10
if (x > 5) {
    println("x is greater than 5")
} else {
    println("x is less than or equal to 5")
}

// Example 2: If-else if-else statement
val y = 7
if (y > 10) {
    println("y is greater than 10")
} else if (y > 5) {
    println("y is greater than 5 but less than or equal to 10")
} else {
    println("y is less than or equal to 5")
}
